#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	n2;
	char	*s2;

	s2 = (char *)s;
	n2 = 0;
	while (n2 < n)
	{
		s2[n2] = c;
		n2++;
	}
	return (s);
}
