#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		x;
	char	*s1;

	s1 = (char *)s;
	x = 0;
	while (c > 256)
		c = c - 256;
	while (s1[x] != c && s1[x])
		x++;
	if (s1[x] == c)
		return (&s1[x]);
	else
		return (NULL);
}
